    public class loadtime implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            if(tg > 0)
            {
                tg--;
                Progress.setValue(tg);
            }else
            {
                lbcauhoi.setText("Kết thúc - Điểm là: "+ diem);
                btcuu.setEnabled(false);
                btsai.setEnabled(false);
                btdung.setEnabled(false);
                //Khi hết thời gian các núi "CỨU ĐÚNG SAI" sẽ bị Enabled riêng "HỒI SINH VẪN CÓ THỂ DÙNG"
            }
        }  
    }
    
    public void load(){
        diem++;
        lbdiem.setText(diem+"");
        btchoi.setText("CHƠI LẠI");
        Random r = new Random();
        a = r.nextInt(dokho) + 1;
        b = r.nextInt(dokho) + 1;
        kt = r.nextInt(11);
        if(kt == 0 || kt == 1 || kt == 2) //0-1-2 25% đúng +
        {
            kq = a + b;
            lbcauhoi.setText(a+" + "+b+" = "+kq);
        }
        else if(kt == 3 || kt == 4 || kt == 5) //3-4-5 25% đúng -
        {
            kq = a - b;
            lbcauhoi.setText(a+" - "+b+" = "+kq);
        }else if(kt == 6 || kt == 7 || kt == 8) //6-7-8 25% sai -
        {
            kq = r.nextInt(dokho*2);
            lbcauhoi.setText(a+" - "+b+" = "+kq);
        }
        else //9-10-11 25% sai +
        {
            kq = r.nextInt(dokho*2);
            lbcauhoi.setText(a+" + "+b+" = "+kq);
        }
        
        if(max < diem)
        {
            max = diem;
            lbkyluc.setText(diem + "");
        }
    }    
       
    
    
    
