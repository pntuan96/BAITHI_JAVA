    private void btsaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaiActionPerformed
        if(ketthuc == false)
        {
            if(diem % 10 == 0 && diem != 0) //Mỗi 10 điểm thì được hồi sinh
            {
                bthoisinh.setEnabled(true);
            }
            if(a + b != kq && a - b != kq)
            {
                load();
                if(diem == 5 || diem == 10 || diem == 15 || diem == 20) //Mỗi 5 câu thời gian sẽ giảm 15%
                {
                    moctg -= 15;
                    btcuu.setEnabled(true);
                }
                tg = moctg;
                time.start();
            }else //Khi trả lời sai đáp án
            {
                ketthuc = true;
                lbcauhoi.setText("Sai rồi");
                btchoi.setText("CHƠI LẠI");
                btchoi.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btsaiActionPerformed

